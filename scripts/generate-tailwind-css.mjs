// Bundle the Tailwind + @maizzle/tailwindcss CSS sources into a single TS
// module so the browser/edge Tailwind compiler can resolve `@import`s
// without filesystem access. Run as part of the build (and whenever those
// packages update): `node scripts/generate-tailwind-css.mjs`.
import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { dirname, resolve, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')
const nm = resolve(root, 'node_modules')

/** package-relative key -> absolute path */
const tailwindDir = resolve(nm, 'tailwindcss')
const maizzleDir = resolve(nm, '@maizzle/tailwindcss')

const files = {}
function add(key, absPath) {
  if (!existsSync(absPath)) {
    throw new Error(`Missing CSS source: ${absPath}`)
  }
  files[key] = readFileSync(absPath, 'utf8')
}

// tailwindcss core CSS — only the files @maizzle/tailwindcss actually imports
// (`tailwindcss/theme` + `tailwindcss/utilities`). The full `tailwindcss`
// entry (index.css) and `preflight.css` aren't bundled: email templates use
// @maizzle/tailwindcss's own reset, not Tailwind's preflight.
for (const f of ['theme.css', 'utilities.css']) {
  add(`tailwindcss/${f}`, resolve(tailwindDir, f))
}

// @maizzle/tailwindcss — read every file listed in its exports + index
const maizzlePkg = JSON.parse(readFileSync(resolve(maizzleDir, 'package.json'), 'utf8'))
const maizzleFiles = new Set(['index.css'])
for (const target of Object.values(maizzlePkg.exports)) {
  maizzleFiles.add(target.replace(/^\.\//, ''))
}
for (const rel of maizzleFiles) {
  add(`@maizzle/tailwindcss/${rel}`, resolve(maizzleDir, rel))
}

// bare-specifier -> canonical key aliases
const aliases = {
  'tailwindcss/theme': 'tailwindcss/theme.css',
  'tailwindcss/utilities': 'tailwindcss/utilities.css',
  '@maizzle/tailwindcss': '@maizzle/tailwindcss/index.css',
}
for (const [spec, target] of Object.entries(maizzlePkg.exports)) {
  if (spec === '.') continue
  aliases[`@maizzle/tailwindcss${spec.slice(1)}`] = `@maizzle/tailwindcss/${target.replace(/^\.\//, '')}`
}

const out = `/* eslint-disable */
// @ts-nocheck
// Generated by scripts/generate-tailwind-css.mjs — do not edit.
// Tailwind + @maizzle/tailwindcss CSS sources for the browser/edge compiler.

export const tailwindCssFiles: Record<string, string> = ${JSON.stringify(files, null, 0)}

export const tailwindCssAliases: Record<string, string> = ${JSON.stringify(aliases, null, 2)}
`

const dest = resolve(root, 'src/utils/tailwindCssSources.generated.ts')
writeFileSync(dest, out)
console.log(`Wrote ${dest} (${Object.keys(files).length} files, ${Object.keys(aliases).length} aliases)`)
console.log(`  → ${relative(root, dest)}`)

// --- Built-in component sources (for the browser SFC compiler) -------------
import { readdirSync } from 'node:fs'

const componentsDir = resolve(root, 'src/components')
const builtins = {}
for (const file of readdirSync(componentsDir).sort()) {
  if (!file.endsWith('.vue')) continue
  const name = file.replace(/\.vue$/, '')
  builtins[name] = readFileSync(resolve(componentsDir, file), 'utf8')
}

const builtinsOut = `/* eslint-disable */
// @ts-nocheck
// Generated by scripts/generate-tailwind-css.mjs — do not edit.
// Built-in Maizzle component SFC sources, compiled at runtime by the
// browser/edge renderer (the Node renderer compiles them via Vite instead).

export const builtinComponentSources: Record<string, string> = ${JSON.stringify(builtins, null, 0)}
`
const builtinsDest = resolve(root, 'src/components/builtinSources.generated.ts')
writeFileSync(builtinsDest, builtinsOut)
console.log(`Wrote ${builtinsDest} (${Object.keys(builtins).length} components)`)
